package day1;

public class Java1_주석 {
	public static void main(String[] args) {
		// 코드에서는 들여쓰기가 중요하다.
		// syso + Ctrl + Space => 텍스트 입력
		// 숫자는 그냥 입력, 문자는 쌍따옴표("") 안에 작성해야 한다.
		System.out.println(1); 
		System.out.println(2); 
		System.out.println("안녕하세요?");
		
		// 주석은 슬래시 두개(//) 직접 입력해도 되고,
		// Ctrl + / 하면 한줄 주석이 된다.
		// ;는 띄어쓰기 용도이다.
		
	} // main
} // class
